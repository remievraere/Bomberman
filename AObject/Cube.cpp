bool Cube::initialize()
{
  if (_texture.load("./Graphics/Game/ThemeSpa/Cube.tga") == false)
    {
      std::cerr << "Cannot load the cube texture" << std::endl;
      return (false);
    }
  createGeometry();
  return (true);
}

bool Cube::initialize(std::string path)
{
  if (_texture.load(path) == false)
    {
      std::cerr << "Cannot load the " << path << " texture" << std::endl;
      return (false);
    }
  createGeometry();
  return (true);
}

void	Cube::update(gdl::Clock const &clock, gdl::Input &input)
{
}

void	Cube::draw(gdl::AShader &shader, gdl::Clock const &clock)
{
  (void)clock;
  _texture.bind();
  _geometry.draw(shader, getTransformation(), GL_QUADS);
}

char		Cube::getT()const
{
  return ('F');
}

std::string	Cube::getData()const
{
  std::ostringstream	data;
  data << "X=" << _position.x << "Z=" << _position.z;
  return (data.str());
}
