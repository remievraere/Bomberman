bool		Bcube::initialize()
{
  if (_texture.load("./Graphics/Game/ThemeSpa/Bcube.tga") == false)
    {
      std::cerr << "Cannot load the Bcube texture" << std::endl;
      return (false);
    }
  createGeometry();
  return (true);
}

bool		Bcube::initialize(std::string path)
{
  if (_texture.load(path) == false)
    {
      std::cerr << "Cannot load the Bcube " << path << " texture" << std::endl;
      return (false);
    }
  createGeometry();
  return (true);
}

void		Bcube::update(gdl::Clock const &clock, gdl::Input &input)
{
}

void		Bcube::draw(gdl::AShader &shader, gdl::Clock const &clock)
{
  (void)clock;
  _texture.bind();
  _geometry.draw(shader, getTransformation(), GL_QUADS);
}

char		Bcube::getT()const
{
  return ('B');
}

std::string	Bcube::getData()const
{
  std::ostringstream	data;
  data << "X=" << _position.x << "Z=" << _position.z;
  return (data.str());
}
