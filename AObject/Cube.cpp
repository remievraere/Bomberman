#include "Cube.hh"

Cube::Cube()
{
}

Cube::Cube(int x, int y, int z)
{
  this->_position.x = x;
  this->_position.y = y;
  this->_position.z = z;
}

Cube::~Cube()
{
}

glm::vec3	Cube::getPos()const
{
  return (this->_position);
}

std::string	Cube::getType()const
{
  return ("Cube");
}

