#include	"Bcube.hh"

Bcube::Bcube()
{
}

Bcube::Bcube(int x, int y, int z)
{
  _position.x = x;
  _position.y = y;
  _position.z = z;
}

Bcube::~Bcube()
{
}

glm::vec3	Bcube::getPos()const
{
  return (_position);
}

std::string	Bcube::getType()const
{
  return ("Bcube");
}

